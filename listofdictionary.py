# Update key:value pairs of a Dictionary in List of Dictionaries

'''myList = [
	{
		'foo':12,
		'bar':14
	},
	{
		'moo':52,
		'car':641
	},
	{
		'doo':6,
		'tar':84
	}
]

# Update value for 'bar' in first dictionary
myList[0]['bar'] = 52

# Add a new key:value pair to second dictionary
myList[1]['gar'] = 38

# Delete a key:value pair from third dictionary
del myList[2]['doo']

print(myList)'''





